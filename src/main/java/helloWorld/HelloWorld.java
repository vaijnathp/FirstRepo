package helloWorld;

public class HelloWorld {

	private String message;

	public HelloWorld(String message) {
		this.message=message;
	}
	
	public void print(){
		System.out.print(message);
	}
	public void println(){
		System.out.println(message);
	}
	
	public void println(){
		System.out.print("*******"+message+"*******");
	}

}
